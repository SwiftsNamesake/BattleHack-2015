import Control.Monad      (forM)
import Control.Concurrent (threadDelay)
import Text.Printf



--------------------------------------------------------------------------------------------------------------------------------------------
-- Entry point
--------------------------------------------------------------------------------------------------------------------------------------------
-- |
main :: IO ()
main = do
  putStrLn "Hello world!"
  putStrLn "Counting down to launch..."
  forM [9,8..0] $ ((>> threadDelay (10^6)) . print)
  putStrLn "Launching. What have I done!?"
